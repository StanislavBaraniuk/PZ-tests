/**
 *
 * Виводить тестове завдання
 */
    public void openTest() {
        list.removeAll(list);
        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);
        jCheckBox4.setSelected(false);
        
        jTextField1.setText(test.get(activeTest).tile);
       
        for (int i=0; i<4; i++) {
            list.add(i);
        }
       
        Collections.shuffle(list);
        
        jTextField2.setText(test.get(activeTest).tests.get(list.get(0)));
        jTextField3.setText(test.get(activeTest).tests.get(list.get(1)));
        jTextField4.setText(test.get(activeTest).tests.get(list.get(2)));
        jTextField5.setText(test.get(activeTest).tests.get(list.get(3)));
    }
  /**
 *
 * Перевірка тестового завдання
 */  
    public void checkTest() {
        int corect = jCheckBox1.isSelected() ? 0 : jCheckBox2.isSelected() ? 1 : jCheckBox3.isSelected() ? 2 : jCheckBox4.isSelected() ? 3 : null;
//        System.out.println(corect + " and " + list.get(corect) + " and " + test.get(activeTest).corect);
        if (list.get(corect) == test.get(activeTest).corect) {
            this.corects++; 
            this.results += "Тест №" + (activeTest+1) + ", обрана відповідь: №" + (corect+1) + " - " + test.get(activeTest).tests.get(test.get(activeTest).corect) + "  ВІРНО\n";
        } else {
            this.results += "Тест №" + (activeTest+1) + ", обрана відповідь: №" + (corect+1)  + " - " + test.get(activeTest).tests.get(list.get(corect)) + "  НЕ ВІРНО\n";
        }
    }
    /**
 *
 * Закриття тестів
 */
    public void closeTest() {
        this.results = "";
        this.corects = 0;
        this.test.removeAll(test);
        this.list.removeAll(list);
        this.activeTest = 0;
        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);
        jCheckBox4.setSelected(false);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        
    }
    
