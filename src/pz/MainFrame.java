    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        closeTest();
        
        jTextArea2.setText("");
        
        ArrayList<String> testSTR = new ArrayList();
        
        switch (jComboBox1.getSelectedIndex()) {
            case 0:
                testSTR = FileWork.getInctanse().read_in_line("tests/1.txt");
                break;
            case 1:
                testSTR = FileWork.getInctanse().read_in_line("tests/2.txt");
                break;
            case 2:
                testSTR = FileWork.getInctanse().read_in_line("tests/3.txt");
                break;
            case 3:
                testSTR = FileWork.getInctanse().read_in_line("tests/4.txt");
                break;
            case 4:
                testSTR = FileWork.getInctanse().read_in_line("tests/5.txt");
                break;
            case 5:
                testSTR = FileWork.getInctanse().read_in_line("tests/6.txt");
                break;
        }

        int tN = 0, vN = 0, k = 0;
        for (int j=0; j < testSTR.size()/5; j++)  {
            Test t = new Test();
            for (int i = k; i < testSTR.size(); i++) {
                if (i == k+5) {
                    k = i;
                    break;
                }
                if (i % 5 == 0 || i == 0) {
                    t.tile = testSTR.get(i);
                } else {
                   
                    t.tests.add(testSTR.get(i));
                }
                
            }
            test.add(t);
        } 
        
        for (int j=0; j < test.size(); j++)  {
            for (int i = 0; i < test.get(i).tests.size(); i++) {
                if (test.get(j).tests.get(i).contains("@")) {
                    String s = removeCharAt(test.get(j).tests.get(i), 0);
                    test.get(j).tests.remove(i);
                    test.get(j).tests.add(i, s);
                    test.get(j).corect = i;
                    System.err.println(test.get(j).corect + " i = " + i);
                }
            }
        }
        
    
        openTest();
        
    }//GEN-LAST:event_jButton1ActionPerformed
    /**
 *
 * Видаляє символ вказуючий на правильну відповідь
 */
    public String removeCharAt(String s, int pos) {
      return s.substring(0, pos) + s.substring(pos + 1);
   }
